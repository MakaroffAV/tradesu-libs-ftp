package ftp

// ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++ //

import (
	"net"
)

// ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++ //

var (
	ftpAddr string = "ftp.zakupki.gov.ru:21"
)

// ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++ //

type FtpConn struct {
	conn net.Conn
}

// ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++ //

func (f FtpConn) Delete() error {
	if err := f.conn.Close(); err != nil {
		panic(err)
	}
	return nil
}

// ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++ //

func createFtpConn() (*FtpConn, error) {

	c, cErr := net.Dial("tcp", ftpAddr)
	if cErr != nil {
		panic(cErr)
	}

	return &FtpConn{conn: c}, nil

}

